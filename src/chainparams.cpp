/**
 * What makes a good checkpoint block?
 * + Is surrounded by blocks with reasonable timestamps
 *   (no blocks before with a timestamp after, none after with
 *    timestamp before)
 * + Contains no strange transactions
 */

class CMainParams : public CChainParams {
public:
    CMainParams() {
        strNetworkID = "main";
        consensus.nSubsidyHalvingInterval = 420000;
        consensus.BIP16Height = 0;
        consensus.BIP34Height = 0;
        consensus.BIP34Hash = uint256S("1bff4bbd83f4cb3fc8341cf2b258da0777b38a1f567ca2dd56367e84d2402d9d"); // main genesis hash
        consensus.BIP65Height = 0;
        consensus.BIP66Height = 0;
        consensus.powLimit = uint256S("00000fffffffffffffffffffffffffffffffffffffffffffffffffffffffffff"); 
        consensus.nPowTargetTimespan = 3 * 24 * 60 * 60; // 3 days
        consensus.nPowTargetSpacing = 10 * 60;
        consensus.fPowAllowMinDifficultyBlocks = false;
        consensus.fPowNoRetargeting = false;
        consensus.nRuleChangeActivationThreshold = 6048; // 75% of 8064
        consensus.nMinerConfirmationWindow = 8064; // nPowTargetTimespan / nPowTargetSpacing * 4

        /**
          Special value for nStartTime indicating that the deployment is always active:
          Consensus::BIP9Deployment::ALWAYS_ACTIVE = -1

          Constant for nTimeout very far in the future:
          Consensus::BIP9Deployment::NO_TIMEOUT: std::numeric_limits<int64_t>::max(); = 9223372036854775807

          getblockchaininfo using ALWAYS_ACTIVE and NO_TIMEOUT:
          "segwit": {
            "status": "active",
            "startTime": -1,
            "timeout": 9223372036854775807,
            "since": 0
           }

          getblockchaininfo using 0 and 999999999999ULL:
          "segwit": {
            "status": "started",
            "bit": 1,
            "startTime": 0,
            "timeout": 999999999999,
            "since": 8064,
            "statistics": {
              "period": 8064,
              "threshold": 6048,
              "elapsed": 3120,
              "count": 3120,
              "possible": true
            }
          }
        */

        consensus.vDeployments[Consensus::DEPLOYMENT_TESTDUMMY].bit = 28;
        consensus.vDeployments[Consensus::DEPLOYMENT_TESTDUMMY].nStartTime = 0;
        consensus.vDeployments[Consensus::DEPLOYMENT_TESTDUMMY].nTimeout = 999999999999ULL;

        // Deployment of BIP68, BIP112, and BIP113.
        consensus.vDeployments[Consensus::DEPLOYMENT_CSV].bit = 0;
        consensus.vDeployments[Consensus::DEPLOYMENT_CSV].nStartTime = 0;
        consensus.vDeployments[Consensus::DEPLOYMENT_CSV].nTimeout = 999999999999ULL;

        // Deployment of SegWit (BIP141, BIP143, and BIP147)
        consensus.vDeployments[Consensus::DEPLOYMENT_SEGWIT].bit = 1;
        consensus.vDeployments[Consensus::DEPLOYMENT_SEGWIT].nStartTime = 0;
        consensus.vDeployments[Consensus::DEPLOYMENT_SEGWIT].nTimeout = 999999999999ULL;

        // The best chain should have at least this much work.
        consensus.nMinimumChainWork = uint256S("0x00");

        // By default assume that the signatures in ancestors of this block are valid.
        consensus.defaultAssumeValid = uint256S("0x00");

        /**
         * The message start string is designed to be unlikely to occur in normal data.
         * The characters are rarely used upper ASCII, not valid as UTF-8, and produce
         * a large 32-bit integer with any alignment.
         */
        pchMessageStart[0] = 0xaa;
        pchMessageStart[1] = 0xed;
        pchMessageStart[2] = 0xc1;
        pchMessageStart[3] = 0xe5;
        nDefaultPort = 3333;
        nPruneAfterHeight = 100000;

        genesis = CreateGenesisBlock(1614638965, 2085694948, 0x1e0ffff0, 1, 50 * COIN);
        consensus.hashGenesisBlock = genesis.GetHash();
        assert(consensus.hashGenesisBlock == uint256S("0x1bff4bbd83f4cb3fc8341cf2b258da0777b38a1f567ca2dd56367e84d2402d9d"));
        assert(genesis.hashMerkleRoot == uint256S("0x977a9b291938b8752ceccae6ec6bb500ee694679a1cd67405fbf6b1fe742ab13"));

        // Note that of those which support the service bits prefix, most only support a subset of
        // possible options.
        // This is fine at runtime as we'll fall back to using them as a oneshot if they don't support the
        // service bits we want, but we should get them updated to support all service bits wanted by any
        // release ASAP to avoid it where possible.
        vSeeds.emplace_back("dnsseed.niftycoin.org");
        //vSeeds.emplace_back("seed-a.niftycoin.loshan.co.uk");
        //vSeeds.emplace_back("dnsseed.thrasher.io");
        //vSeeds.emplace_back("dnsseed.niftycointools.com");
        //vSeeds.emplace_back("dnsseed.niftycoinpool.org");
        //vSeeds.emplace_back("dnsseed.koin-project.com");

        base58Prefixes[PUBKEY_ADDRESS] = std::vector<unsigned char>(1,53);
        base58Prefixes[SCRIPT_ADDRESS] = std::vector<unsigned char>(1,5);
        base58Prefixes[SCRIPT_ADDRESS2] = std::vector<unsigned char>(1,50);
        base58Prefixes[SECRET_KEY] =     std::vector<unsigned char>(1,53);
        base58Prefixes[EXT_PUBLIC_KEY] = {0x4E, 0x49, 0xB2, 0x1E};
        base58Prefixes[EXT_SECRET_KEY] = {0x4E, 0x49, 0xAD, 0xE4};

        bech32_hrp = "nfy";

        vFixedSeeds = std::vector<SeedSpec6>(pnSeed6_main, pnSeed6_main + ARRAYLEN(pnSeed6_main));

        fDefaultConsistencyChecks = false;
        fRequireStandard = true;
        fMineBlocksOnDemand = false;

        checkpointData = {
            {
                { 0,     uint256S("0x1bff4bbd83f4cb3fc8341cf2b258da0777b38a1f567ca2dd56367e84d2402d9d")},
                //{ 2000,  uint256S("0xfae783f593105c8e9343f944a546f0dd7324df3efe0e2428a364ea27578b4e3d")},
                //{ 4477,  uint256S("0x5fa0b0472ed3604c8ff179a98767bc1557291e71a17866e06c28bc9d64e236f4")},
                //{ 10712, uint256S("0x6b8fb2515be4d9a0c3ad1fa1d2d208b36d51a8137d02b65d225b04e832ca14da")},
            }
        };

        chainTxData = ChainTxData{
            /* nTime    */ 1614638965,
            /* nTxCount */ 0,
            /* dTxRate  */ 0.0
        };

        /* disable fallback fee on mainnet */
        m_fallback_fee_enabled = false;
    }
};

